public class Arithmatic {
    public static void main(String[] args) {
        int a = 10, b = 20;

        int Sum = a + b;

        int Different = a - b;

        int Multiple = a * b;

        int Divition = b / a;

        int modulo = a % b;

        System.out.println("Addition of a & b Value : " + Sum);
        System.out.println("Different of a & b Value : " + Different);
        System.out.println("Multiple of a & b Value : " + Multiple);
        System.out.println("Divition of a & b Value : " + Divition);
        System.out.println("Mod value of a & b : " + modulo);
        
    }
    
}
