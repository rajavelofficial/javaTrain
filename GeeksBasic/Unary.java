public class Unary {
    public static void main(String[] args){
        int a = 10;
        int b = 20;

        System.out.println("Increment Operand Operations : ");
        System.out.println( "" + (a++));
        System.out.println( (++a) + "\n" );

        System.out.println("Decrement Operand Operations : ");
        System.out.println( "" + (b--));
        System.out.println( "" + (--b));

    }
    
}
